package pattern;

public class pattern1 {
    public static void main(String[] args) {
        int n;
        Pattern1(5);

    }
    public static void Pattern1(int n){
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
