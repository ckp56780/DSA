package pattern;

public class pattern2 {
    public static void main(String[] args) {
        int n;
        Pattern2(3);
    }

      public static void Pattern2(int n){
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
